        fill_v = fill_verde if bueno else fill_rojo if var != 0 else None
        f_color = verde if bueno else rojo if var != 0 else negro
        ref = ('✓ Mejora' if bueno else '⚠ Peor') if var != 0 else '= Sin cambio'
