def database_url():
    if not DATABASE_URL:
        return ''
    if 'sslmode=' in DATABASE_URL:
        return DATABASE_URL
    separator = '&' if '?' in DATABASE_URL else '?'
    return DATABASE_URL + separator + 'sslmode=require'


def use_database():
    return bool(DATABASE_URL)


def get_db():
    if psycopg is None:
        raise RuntimeError('psycopg no está instalado. Añade psycopg[binary] a requirements.txt')
    return psycopg.connect(database_url(), row_factory=dict_row)


def json_number(value):
    if isinstance(value, Decimal):
        as_float = float(value)
        return int(as_float) if as_float.is_integer() else as_float
    return value


def normalize_kpi(row):
    return {
        'id': row['id'],
        'nombre': row['nombre'],
        'equipo': row['equipo'],
        'descripcion': row.get('descripcion'),
        'formula': row.get('formula'),
        'unidad': row.get('unidad'),
        'frecuencia': row.get('frecuencia'),
        'objetivo': json_number(row.get('objetivo')),
        'minimo': json_number(row.get('minimo')),
        'excelente': json_number(row.get('excelente')),
        'peso': json_number(row.get('peso')),
        'activo': row.get('activo', True),
    }


def ensure_kpis_table():
    global _kpis_table_ready
    if _kpis_table_ready or not use_database():
        return

    with get_db() as conn:
        with conn.cursor() as cur:
            cur.execute("""
                create table if not exists kpis (
                    id bigint generated by default as identity primary key,
                    nombre text not null,
                    equipo text not null,
                    descripcion text,
                    formula text,
                    unidad text,
                    frecuencia text,
                    objetivo numeric,
                    minimo numeric,
                    excelente numeric,
                    peso numeric,
                    activo boolean not null default true,
                    created_at timestamptz not null default now(),
                    updated_at timestamptz not null default now()
                )
            """)
            cur.execute("select count(*) as total from kpis")
            if cur.fetchone()['total'] == 0:
                for kpi in load_kpis_file().get('kpis', []):
                    cur.execute("""
                        insert into kpis
                        (id, nombre, equipo, descripcion, formula, unidad, frecuencia,
                         objetivo, minimo, excelente, peso, activo)
                        values (%s, %s, %s, %s, %s, %s, %s, %s, %s, %s, %s, %s)
                    """, (
                        kpi.get('id'),
                        kpi.get('nombre'),
                        kpi.get('equipo'),
                        kpi.get('descripcion'),
                        kpi.get('formula'),
                        kpi.get('unidad'),
                        kpi.get('frecuencia'),
                        kpi.get('objetivo'),
                        kpi.get('minimo'),
                        kpi.get('excelente'),
                        kpi.get('peso'),
                        kpi.get('activo', True),
                    ))
                cur.execute("""
                    select setval(
                        pg_get_serial_sequence('kpis', 'id'),
                        coalesce((select max(id) from kpis), 1),
                        true
                    )
                """)
    _kpis_table_ready = True


def load_kpis():
    if not use_database():
        return load_kpis_file()

    ensure_kpis_table()
    equipo = request.args.get('equipo')
    with get_db() as conn:
        with conn.cursor() as cur:
            if equipo and equipo != 'Todos':
                cur.execute(
                    "select * from kpis where activo = true and equipo = %s order by id",
                    (equipo,)
                )
            else:
                cur.execute("select * from kpis where activo = true order by id")
            return {'kpis': [normalize_kpi(row) for row in cur.fetchall()]}


def save_kpis(data):
    save_kpis_file(data)


def insert_kpi(nuevo):
    if not use_database():
        data = load_kpis_file()
        nuevo['id'] = max((k['id'] for k in data['kpis']), default=0) + 1
        nuevo['activo'] = True
        data['kpis'].append(nuevo)
        save_kpis_file(data)
        return nuevo

    ensure_kpis_table()
    with get_db() as conn:
        with conn.cursor() as cur:
            cur.execute("""
                insert into kpis
                (nombre, equipo, descripcion, formula, unidad, frecuencia,
                 objetivo, minimo, excelente, peso, activo)
                values (%s, %s, %s, %s, %s, %s, %s, %s, %s, %s, true)
                returning *
            """, (
                nuevo.get('nombre'),
                nuevo.get('equipo'),
                nuevo.get('descripcion'),
                nuevo.get('formula'),
                nuevo.get('unidad'),
                nuevo.get('frecuencia'),
                nuevo.get('objetivo'),
                nuevo.get('minimo'),
                nuevo.get('excelente'),
                nuevo.get('peso'),
            ))
            return normalize_kpi(cur.fetchone())


def update_kpi_record(kpi_id, actualizado):
    if not use_database():
        data = load_kpis_file()
        for i, k in enumerate(data['kpis']):
            if k['id'] == kpi_id:
                data['kpis'][i] = {**k, **actualizado, 'id': kpi_id}
                save_kpis_file(data)
                return data['kpis'][i]
        return None

    ensure_kpis_table()
    with get_db() as conn:
        with conn.cursor() as cur:
            cur.execute("""
                update kpis
                set nombre = %s,
                    equipo = %s,
                    descripcion = %s,
                    formula = %s,
                    unidad = %s,
                    frecuencia = %s,
                    objetivo = %s,
                    minimo = %s,
                    excelente = %s,
                    peso = %s,
                    updated_at = now()
                where id = %s and activo = true
                returning *
            """, (
                actualizado.get('nombre'),
                actualizado.get('equipo'),
                actualizado.get('descripcion'),
                actualizado.get('formula'),
                actualizado.get('unidad'),
                actualizado.get('frecuencia'),
                actualizado.get('objetivo'),
                actualizado.get('minimo'),
                actualizado.get('excelente'),
                actualizado.get('peso'),
                kpi_id,
            ))
            row = cur.fetchone()
            return normalize_kpi(row) if row else None


def delete_kpi_record(kpi_id):
    if not use_database():
        data = load_kpis_file()
        data['kpis'] = [k for k in data['kpis'] if k['id'] != kpi_id]
        save_kpis_file(data)
        return

    ensure_kpis_table()
    with get_db() as conn:
        with conn.cursor() as cur:
            cur.execute(
                "update kpis set activo = false, updated_at = now() where id = %s",
                (kpi_id,)
            )


def parse_num(s):
    try:
        return float(str(s).replace('.', '').replace(',', '.'))
    except:
        return 0.0


def load_csv(path):
    with open(path, encoding='latin-1') as f:
        content = f.read().replace('\r\n', '\n').replace('\r', '\n')
    reader = csv.DictReader(io.StringIO(content), delimiter=';')
    return list(reader)


